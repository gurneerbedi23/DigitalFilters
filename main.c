void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	input_sample = HAL_ADC_GetValue(&hadc1);

	start = ARM_CM_DWT_CYCCNT;
#if METHOD == 'A'
// code method A

	state[0] = (float32_t)input_sample;
	for (i = 0; i <N; i++)
	{
		pn += (h[i] * state[i]);

	}
	for (i = N-1; i >0; i--)
	{
		state[i] = state[i-1];
	}


#elif METHOD == 'B'

	*state_ptr = (float32_t)input_sample;
	for (i = 0; i < N ; i++)
	{
		pn += *(coeff_ptr + i) * *state_ptr; //multiply and accumulate
		if (++state_ptr > end_ptr) //increment, if past the end pointer, wrap back to start pointer
			state_ptr = start_ptr;
	}
	if (--state_ptr < start_ptr) //after the last increment, you need to decrement once to go back to oldest sample
		state_ptr = end_ptr; //if you went past start pointer, wrap back to end pointer

#endif

	stop  = ARM_CM_DWT_CYCCNT;
	delta = stop - start;//calculating difference between start and stop
	//certain filters can eliminate DC components and make the output negative.
	//Centering signal at middle of 12 bits (at 2048). Note this assumes we limit our input to 11 bits instead of 12 bits
	pn = pn+2048;
	if(pn>4000) pn=4000; //certain filters can amplify at certain frequencies. Preventing overflow
	if(pn<0) pn=0; //Preventing underflow
	output_sample=(short)pn;

	output_sample=output_sample<<2; //upshift by 2
	spibytes[0]=output_sample>>8; //split output into 2 bytes
	spibytes[1]=output_sample;


	 GPIOA->BSRR |=(1<<3)<<16; //enable sync
	 HAL_SPI_Transmit(&hspi1,spibytes, 2, 2); //transmit to SPI 1
	 GPIOA->BSRR |=(1<<3); //disable sync


	 pn=0.0; //reset accumulator
	 output_sample=0; //reset output

	 HAL_ADC_Start_IT (&hadc1);

}



